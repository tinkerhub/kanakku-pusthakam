import fs from "node:fs/promises";

const source = process.env.OPENAPI_SCHEMA_URL ?? "../openapi-schema.json";
const output = new URL("../src/generated/api.ts", import.meta.url);

async function readSchema() {
  if (source.startsWith("http://") || source.startsWith("https://")) {
    const response = await fetch(source);
    if (!response.ok) {
      throw new Error(`Failed to fetch schema: ${response.status}`);
    }
    return response.json();
  }
  const fileUrl = new URL(source, import.meta.url);
  return JSON.parse(await fs.readFile(fileUrl, "utf8"));
}

function literal(value) {
  return JSON.stringify(value, null, 2);
}

const schema = await readSchema();
const paths = Object.keys(schema.paths ?? {}).sort();
const tags = Array.from(
  new Set(
    paths.flatMap((path) =>
      Object.values(schema.paths[path] ?? {}).flatMap((operation) => operation.tags ?? []),
    ),
  ),
).sort();

const contents = `// Generated by scripts/generate-api-client.mjs from OpenAPI.
// Keep this file checked in so first-party and third-party frontends share route constants.

export const openApiInfo = ${literal({
  title: schema.info?.title ?? "Makerspace Manager API",
  version: schema.info?.version ?? "0.0.0",
})} as const;

export const openApiTags = ${literal(tags)} as const;

export const openApiPaths = ${literal(paths)} as const;

export type ApiPath = (typeof openApiPaths)[number];

export class MakerspaceApiClient {
  constructor(
    private readonly baseUrl: string,
    private readonly getHeaders: () => HeadersInit = () => ({}),
  ) {}

  async request<T>(path: ApiPath | string, options: RequestInit = {}): Promise<T> {
    const response = await fetch(\`\${this.baseUrl}\${path}\`, {
      ...options,
      headers: {
        "Content-Type": "application/json",
        ...this.getHeaders(),
        ...(options.headers ?? {}),
      },
    });
    if (!response.ok) {
      const body = await response.json().catch(() => ({}));
      throw new Error(body.detail ?? \`Request failed (\${response.status})\`);
    }
    if (response.status === 204) {
      return undefined as T;
    }
    return (await response.json()) as T;
  }
}
`;

await fs.mkdir(new URL("../src/generated/", import.meta.url), { recursive: true });
await fs.writeFile(output, contents);
console.log(`Generated ${output.pathname} with ${paths.length} paths.`);
